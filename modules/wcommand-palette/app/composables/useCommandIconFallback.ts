  const generateFallbackIcon = (command: Command): string => {
    if (command.icon) return command.icon

    const title = command.title.toLowerCase()
    const keywords = command.keywords?.map(k => k.toLowerCase()) || []

    for (const keyword of [...title.split(' '), ...keywords]) {
      if (iconMap[keyword]) {
        generatedIcons.value[command.id] = iconMap[keyword]
        return iconMap[keyword]
      }
    }

    const defaultIcon = '⚡'
    generatedIcons.value[command.id] = defaultIcon
    return defaultIcon
  }

  const getGeneratedIcon = (commandId: string): string | null => {
    return generatedIcons.value[commandId] || null
  }

  const clearGeneratedIcons = () => {
    generatedIcons.value = {}
  }

  return {
    generatedIcons: readonly(generatedIcons),
    iconMap,
    generateFallbackIcon,
    getGeneratedIcon,
    clearGeneratedIcons
  }
